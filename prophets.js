  async function getProphetData(url) {
    try {
        const response = await fetch(url); // Store the fetch response
        if (!response.ok) {
            throw new Error(`HTTP error! Status: ${response.status}`);
        }

        const data = await response.json(); // Convert response to JSON and store in "data"
        console.table(data.prophets); // Log the data as a table for debugging
        return data;
    } catch (error) {
        console.error('Error fetching prophet data:', error);
        throw error;
    }
}

